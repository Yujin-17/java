package variable_quiz;

public class Quiz01 {

	public static void main(String[] args) {

		String name = "이유진";
		int age = 25;
		
		System.out.println("이름: " + name + " 나이: "+ age);
		
		int a = 3;
		int b = 5;
		
		System.out.println(a + "+" + b  + "=" + (a + b));
		System.out.println(a + "-" + b  + "=" + (a - b));
		
		int c = 7;
	
		System.out.println();
		System.out.println(c +  "*" + "1" + "=" + (c * 1));
		System.out.println(c +  "*" + "2" + "=" + (c * 2));
		System.out.println(c +  "*" + "3" + "=" + (c * 3));
	}

}
